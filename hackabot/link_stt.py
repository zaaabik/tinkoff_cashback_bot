def parse(req, user_id):
    req = ''.join(x for x in req if x.isalpha() or x == ' ')
    req = req.lower().split()
    tmp = None
    skip = False
    for i in req:
        if i in funcs:
            if i == 'хочу':
                tmp = Near
                continue
            else:
                funcs[i](req, user_id)
                skip = True
                break
        if tmp:
            tmp(req, user_id)
            skip = True
    if not skip:
        print('Я вас не понял')
